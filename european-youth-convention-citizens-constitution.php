        <h1 id="constitution-title" style="font-family:Montserrat !important; font-size:2em !important; text-align:center; color:#2B292E !important; margin-bottom:40px !important; margin-top:40px !important;"><strong>THE EUROPEAN YOUTH CONVENTION CITIZENS’ CONSTITUTION</strong></h1>

        <div id="tabs">

          <div id="tabs-0">
            <h2><strong>Preamble</strong></h2>
            <p>United in diversity, we, the European citizens:
                <br><br>
              <b>1.</b> Want a European Federation based on parliamentarism, freedom, equality, democracy, identity, and the rule of law.
                <br><br>
              <b>2.</b> Are committed to the promotion of prosperity, solidarity, sustainability, and transparency.
                <br><br>
              <b>3.</b> Act to protect and reinforce the various European, national, regional, local, and individual identities which belong to our cultural heritage.
                <br><br>
              <b>4.</b> Affirm that the European Union commits to the empowerment of citizens and the preservation of cultural heritage, as well as the environmental sustainability for future generations.
                <br><br>
              <b>5.</b> Are deeply convinced that an inclusive citizenship and sovereignty of European people can only be achieved by advocating effective complementarity between rights and duties.
                <br><br>
              <b>6.</b> Are seeking  for respect of human rights, to announce the European Union as a Federation to defend common interest, values and identities, inspiring actions towards other countries of the world.
                <br><br>
              <b>7.</b> State that the European Union pledges to preserve and reinforce the rights of the European people, in accordance with respect of universal values, and with reference to the United Nations Declaration on Human Rights.
            </p>
          </div>

          <div id="tabs-1">
            <h2><strong>Chapter I: On the Values of the European Union</strong></h2>
                <br>
            <p>
              <b>1.</b> The European Union is founded on the untouchable values of peace, respect for human dignity, freedom, equality, sustainable development, environmental rights, equality of chances, diversity, and respect for human rights, including the rights of persons belonging to minorities and promotes them actively.
                <br><br>
              <b>2.</b> These values are common to the Member States in a society in which pluralism, non-discrimination, tolerance, openness, justice, solidarity and equal rights and chances between all genders identities are paramount.
                <br><br>
              <b>3.</b>  The European Union believes and is based on the rule of law, transparency, constructive understanding and bottom-up democracy.
                <br><br>
              <b>4.</b>  The freedom of movement of people, goods, services, and capital are essential to the dynamic of the Union, its Member States and citizens.
                <br><br>
              <b>5.</b>  	Civic and youth engagement and participation shall be encouraged. European values should be promoted through education in every Member State to empower European citizens and foster their critical thinking.
                <br><br>
              <b>6.</b>  	The European Union fosters solidarity between States and within Member States.
                <br><br>
              <b>7.</b>  The European Union commits to respecting and implementing these values and using them as guidelines in its policies.
            </p>
          </div>

          <div id="tabs-2">
            <h2><strong>Chapter II: On the Citizenship to the European Union</strong></h2>
            <br>
            <p>
              <b>1.</b> The European citizenship is a fully fledged citizenship which entitles its citizens to rights and duties.
                <br><br>
              <b>2.</b> Any individual living in the European Union for a defined period of time, and contributing to the Union’s economic or societal life can request to become a citizen of the European Union, provided that this individual commits to respecting and promoting its values and rights.
                      <br>The rights and duties of a European Citizen are further defined in a European Union’s Citizen Rights Charta which sustains all articles of the human rights Charta. Real free movement of persons will be secured, the right to work, social protection, political rights, and the fundamental freedoms.
                <br><br>
              <b>3.</b> A European holiday on the 9th of May embodies the celebration of unity of the European citizens.
                <br><br>
              <b>4.</b> The symbols of the European Union are: the European flag, a motto “United in diversity”, and the European anthem.
            </p>
          </div>

          <div id="tabs-3">
            <h2><strong>Chapter III: On the Membership to the European Union</strong></h2>
            <br>
            <p>
              <b>1.</b> The European Union is a Federation of States.
                <br><br>
              <b>2.</b> The candidate country shall comply with the values, economic, and social principles of the European Union, as defined in the present Constitution.
                      <br><b>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp a)</b> &nbsp Strict membership criteria shall be further developed in secondary law.
                      <br><b>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp b)</b> &nbsp The authority of granting the criteria lies within the legislative and executive powers of the European Union.
                <br><br>
              <b>3.</b> Both candidate countries and Member States commit to full integration in the European Union, no opting out should be granted especially regarding compliance with European values and economic and social standards, as defined in the chapters 7 and 9 of the Constitution.
                <br><br>
              <b>4.</b> Candidate countries cannot accede without fulfilling all criteria for becoming a member of the European Union. The criteria for associated partnership to the European Union shall be further developed in law.
            </p>
          </div>

          <div id="tabs-4">
            <h2><strong>Chapter IV: On the Institutions of the European Union</strong></h2>
            <br>
            <p>
              <b>1.</b> The legislative body of the European Union is bicameral. It is composed of the European Parliament and the European Senate.
                <br><br>
              <b>2.</b> The European Parliament and the European Senate have the same legislative power on European policies. They can initiate laws and their approvals are taken by majority.
                <br><br>
              <b>3.</b> The European Parliament, representing the people, is composed of members directly elected by citizens for a 5-year mandate.
                <br><br>
              <b>4.</b> The European Senate represents the Member States and the regions of the European Union and is composed of an equal number of delegates from each Member State.
                <br><br>
              <b>5.</b> The European Government is the executive body of the European Union. It has the executive power and it is composed of European Ministers.
                <br><br>
              <b>6.</b> The Head of the European Government is elected by the European Parliament based on the majority of the European elections and can be re-elected.
                      <br>The Head of the European Government proposes the European Ministers and each of them needs the majority of the European Parliament to be appointed.
                <br><br>
              <b>7.</b> The number of European Ministers can be flexible according to the agenda.
                <br><br>
              <b>8.</b> The European Court of Justice is the judicial body of the EU ensuring constitutionality of the European Union’s legal acts. The European Court of Justice can be appealed directly by the citizens, States or European Institutions.
                    <br>In case of violation of the Constitution, sanctions may be applied to Member States. These sanctions can either be monetary or excluding in nature.
                <br><br>
              <b>9.</b> Multi-level governance and subsidiarity are the basis for all actions taken.
            </p>
          </div>

          <div id="tabs-5">
            <h2><strong>Chapter V: On the Electoral Rules of the European Union</strong></h2>
            <br>
        <p>
          <b>1.</b> Every European citizen of legal age has the right to vote and run for election, regardless of gender, nationality, religion, ethnicity, sexual orientation, or any other source of discrimination.
            <br><br>
          <b>2.</b> Citizens of the European Union should be able to vote in whichever country, based on their residence for local, regional, national and European elections, if they meet criterias defined by European institutions.
            <br><br>
          <b>3.</b> A common age for voting should be established through European law by the European Parliament.
            <br><br>
          <b>4.</b> The elections of the European Parliament should take place as follows:
                <br><b>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp a)</b> &nbsp The electoral law to be applied is decided by the European Parliament.
                <br><b>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp b)</b> &nbsp The European political parties should present candidates on a transnational list.
                  <br>The electoral law to be applied is a mixed system: half of the Members of the European Parliament seated at the European Parliament are elected on a constituency basis, and the other half are elected on transnational lists.
                  <br>Transnational lists should replace the existing national lists and a threshold should be introduced and determined by the European Parliament.
                <br><br>
                <br><b>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp c)</b> &nbsp To be eligible as a Member of Parliament, a person has to be a citizen of the European Union.
                <br><b>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp d)</b> &nbsp The European elections should take place on the same day in all Member States of the European Union, which accordingly is a European public holiday.
                <br><b>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp d)</b> &nbsp European political parties should have a legal status.
        </p>
          </div>

          <div id="tabs-6">
            <h2><strong>Chapter VI: On the Democratic Principles of the European Union</strong></h2>
            <br>
            <p>
              <b>1.</b> Each citizen should have the right to be represented equally and fairly in the European institutions. Minorities’ interests need to be accounted for in the democratic process.
                <br><br>
              <b>2.</b> The European Union ensures the participation of citizens in the decision-making process according to the principle of subsidiarity. The use of the Citizen’s initiative shall be expanded by easing its admissibility requirements.
                <br><br>
              <b>3.</b> The European Union puts in place digital tools of e-governance to foster participation among citizens.
                <br><br>
              <b>4.</b> The constitution shall promote fundamental rights, democracy, freedom of speech, freedom of press and freedom of association, and establish a transparent governance that shall be ensured by the European institutions.
                <br><br>
              <b>5.</b> There shall be the right to a direct dialogue between decision makers, citizens, and civil societies.
                <br><br>
              <b>6.</b> Subsidiarity should ensure efficiency in-between the different levels of government.
                <br><br>
              <b>7.</b> There should be more transparency in the decision-making process and European institutions should be held accountable. Meetings of the legislative bodies shall be public. It also guarantees transparency in all lobbying activities related to European institutions.
                <br><br>
              <b>8.</b> Each citizen should have the right to a fair trial.
                <br><br>
              <b>9.</b> To ensure fairness, transparency and the democratic aspect of the European Union, the principle of checks and balances has to be ensured. To do so, the body in-charge includes members of the civil society.
            </p>
          </div>

          <div id="tabs-7">
            <h2><strong>Chapter VII: On the Economic Principles of the European Union</strong></h2>
            <br>
            <p>
              <b>1.</b> The European Union pursues a sustainable development of its economy and an inclusive growth.
                <br><br>
              <b>2.</b> The long term perspective is to put in place a sustainable welfare system and to reach a clean society based on renewables sources.
                <br><br>
              <b>3.</b> The European Union economy must be driven by the principle of circular economy and the respect of green fair trade standards.
                <br><br>
              <b>4.</b> The European Union considers that competition and trade should be fostered respecting the values defined in the present Constitution both for internal and external markets.
                <br><br>
              <b>5.</b> The European Union should favour transnational exchanges among its Member States to encourage common projects for innovation.
                <br><br>
              <b>6.</b> The European Union fights corruption and tax avoidance. It pursues harmony among fiscal systems of its Member States.
                <br><br>
              <b>7.</b> The European Union shall reduce inequalities with regards to the economic development and promote solidarity.
                <br><br>
              <b>8.</b> The EU shall protect the quality and accessibility of public goods such as water, biodiversity, clean air, education, and knowledge.
            </p>
          </div>

          <div id="tabs-8">
            <h2><strong>Chapter VIII: On the Budget of the European Union</strong></h2>
            <br>
            <p>
              <b>1.</b> The budget of the European Union shall be sufficiently vested with resources to enable the Union to achieve its objectives assigned to it by the present Constitution.
                <br><br>
              <b>2.</b> The European Union shall base its sources of revenue and its expenditures on the principle of budgetary subsidiarity meaning that the budget is managed on the most cost-effective level.
                <br><br>
              <b>3.</b> The European Union shall have the power to impose taxes on matters whose effective taxation cannot be achieved by the Member States. This shall not affect the arrangement of the contributions by the Member States  to the European Union’s budget.
The European Union has a sovereign budget based on its own resources, which include taxation, customs, tariffs, and most importantly is proportionally contributed by Member States according to  their economic capabilities.
                <br><br>
              <b>4.</b> The European Union’s budget is drafted and executed by the European Government, independent from Member States’ influence. The legislative bodies of the European Union approve and decide on the budget. The judiciary carries out the evaluation and audit of the budget. The planning, oversight, and execution of the European Union’s budget requires the full involvement of the European Parliament.
                <br><br>
              <b>5.</b> The European Union’s budget is balanced and its management is transparent. The European institutions proposing an action with budgetary implications conduct an assessment of the costs in order to achieve a sustainable budget of the Union.
                    <br>In extraordinary circumstances, the European Union may accumulate debt and derogate temporarily to this provision. A structural debt regime of the European Union is excluded.
                <br><br>
              <b>6.</b> The European institutions ensure a sufficient consultation of the Public in the budgetary process. The European institutions ensure a transparent process and disclose all relevant information.
            </p>
          </div>

          <div id="tabs-9">
            <h2><strong>Chapter IX: On the Social principles of the European Union</strong></h2>
            <br>
            <p>
              <b>1.</b> The European Union pursues harmonisation of fair social standards in every Member State in order to strengthen the European identity. Based on solidarity, equality, and inclusion, economic resources are redistributed between Member States to enhance social standards.
                <br><br>
              <b>2.</b> The European Union shall ensure the right to decent working conditions for every citizen. The workers’ right to organise themselves in unions and transnational trade unions shall be recognised by the European Union.
                <br><br>
              <b>3.</b> Education is a right, it is free and accessible to every citizen. Education shall be compulsory.
                <br><br>
              <b>4.</b> Apprenticeship and practical skills shall be valued to empower young people with no higher education and to tackle youth unemployment.
                <br><br>
              <b>5.</b> The European Union promotes social mobility to further cultural and knowledge exchanges amongst its citizens.
                <br><br>
              <b>6.</b> Everyone shall have the right to receive high quality medical treatment. The European Union establishes a common high standard on access to health care.
                <br><br>
              <b>7.</b> The European Union shall strive towards a welfare society that ensures every individual with the rights to equal access to education at all levels, social security and protection, equal access to high quality health care, and access to decent housing.
                <br><br>
              <b>8.</b> Nobody should be discriminated, regardless of the individual’s country of origin or residence, nationality, gender, sexual orientation, ethnic or religious background, disability, political views or any other reason. Minorities and groups suffering from discrimination shall be protected.
                <br><br>
              <b>9.</b> All citizens should have a right to a basic income and health insurance.
            </p>
          </div>

          <div id="tabs-10">
            <h2><strong>Chapter X: On the Home Affairs, Security, and Justice of the European Union</strong></h2>
            <br>
            <p>
              <b>1.</b> Permanent structured cooperation should be established between Member States in the fields of home affairs and security.
                <br><br>
            </p>
              <h3><strong>Title I: Home Affairs</strong></h3>
              <br><br>
            <p>
              <b>2.</b> The European Union is open to legal immigration regulated by standardised European immigration laws.
                <br><br>
              <b>3.</b> The European Union provides a common asylum system based on humanitarian principles. It provides asylum to non-EU citizens. Member States are obliged to host refugees and immigrants according to a proportional repartition according to their economic capacities. For this, they receive financial contributions from the European Union.
                    <br>The European Union must effectively coordinate the handling of refugees in all its Member States with cooperation and understanding between Member States.
                <br><br>
              <b>4.</b> The European Union provides social policies tackling migrant integration and asylum issues.
                <br><br>
            </p>
              <h3><strong>Title II: Security</strong></h3>
              <br><br>
            <p>
              <b>5.</b> By security it is understood “human security”. The notion of human security aims to encompass all features related to the individual’s integrity and security, broadly mentioned as economic, environmental, food, health, political, community, and personal security. The concept of e-democracy, i.e. the implication of citizens through digital tools, is included as a concrete feature of human security.
                <br><br>
              <b>6.</b> The European Union collects data-based information relevant to the prevention and prosecution of crime.
                    <br>The European Union provides a right to be forgotten online in order to ensure that data can be erased after a certain time.
                <br><br>
              <b>7.</b> Member States of the European Union share information to ensure the prevention and prosecution of crime, while respecting the values of the present Constitution and protecting privacy.
                <br><br>
              <b>8.</b> The European Union employs a common European Intelligence Service (EIS) and a European Agency of Cyber Protection (EACP). These structures respect the subsidiarity principle and aim to fight criminal activities.
                    <br>The European Agency of Cyber Protection aims to protect private data of individuals and organisations and has the objectives to prevent the publication of false information, and to protect citizens and democracy.
                <br><br>
              <b>9.</b> The European Union employs a European Police Force (EPF) which supports the European Intelligence Service and the European Agency of Cyber Protection in their activities. It respects the subsidiarity principle and is accountable to the European legislative bodies.
                <br><br>
            </p>
              <h3><strong>Title III: Justice</strong></h3>
                <br><br>
            <p>
              <b>10.</b> The European Union should envisage a gradual harmonisation of the judicial system to ensure social equality and Justice.
                <br><br>
              <b>11.</b>  An independent Court ensures the universal interpretation and implementation of law across the European Union.
                <br><br>
              <b>12.</b> The European Union ensures transparency and access to information in its decision-making process.
                <br><br>
              <b>13.</b> The European institutions guarantee transparency in all lobbying activities related to European institutions.
                <br><br>
              <b>14.</b> An independent and external supervisory authority of the European Union prevents and investigates corruption.
                <br><br>
              <b>15.</b> The European Union aims to ensure social equality in order to prevent crime.
                <br><br>
              <b>16.</b> The European Union provides common European prison standards. These prison standards guarantee the respect of Human Rights and human dignity, and aim to reintegrate prisoners into the society.
                <br><br>
              <b>17.</b> A European Public Prosecutor is in-charge of the litigations and prosecutes corruption.
                <br><br>
              <b>18.</b> The European Union provides a European Border Control Guard which is responsible for the protection of its external borders.
            </p>
          </div>

          <div id="tabs-11">
            <h2><strong>Chapter XI: On the External Affairs of the European Union</strong></h2>
            <br>
            <p>
              <b>1.</b> The External Affairs and Foreign Policy is an exclusive competence of the European Union.
                <br><br>
              <b>2.</b> A Foreign Affairs Minister of the European Union, elected by the European Parliament and accountable to it, is in-charge of the implementation of foreign policy.
                <br><br>
              <b>3.</b> The European Union is exclusively represented by European Union’s embassies abroad and has a single seat in the international institutions such as the United Nations’ Organisation (UNO).
                <br><br>
              <b>4.</b> The European Union is a full member of the Security Council of the United Nations’ Organisation on behalf of its Member States. The use of the veto right is based on the European Union’s Foreign Policy objectives.
                <br><br>
              <b>5.</b> The European Union ensures stable and peaceful relationships across the world, as well as with its neighbours.
                <br><br>
              <b>6.</b> The European Union aims to develop fair and equal trade agreements with third countries that are beneficial for both trade partners.
                <br><br>
              <b>7.</b> The European Union supports the sustainable development of its partners willing to improve the living conditions of their population.
                <br><br>
              <b>8.</b> The European Union pursues the achievement of the United Nations' Sustainable Development Goals.
                <br><br>
              <b>9.</b> The European Union shall promote international ocean governance to preserve ecosystems and marine resources beyond the national jurisdiction.
            </p>
          </div>

          <div id="tabs-12">
            <h2><strong>Chapter XII: On the Common Defense of the European Union</strong></h2>
            <br>
            <p>
              <b>1.</b> There is one unified European Army under the command of the European Union.
                <br><br>
              <b>2.</b> The head of the European Army is the European Defense Minister.
                <br><br>
              <b>3.</b> The European Army ensures security of the European Union and its borders.
                <br><br>
              <b>4.</b> The European Army relies on the principles of efficient co-operation, shared forces, and shared knowledge.
                <br><br>
              <b>5.</b> The European Defense policy aims at ensuring the protection of its citizens and territory while respecting international law.
                <br><br>
              <b>6.</b> The European Union may engage in humanitarian and peacekeeping missions mandated by the United Nations’ Organisation, jointly with its partners of the North Atlantic Treaty Organisation (NATO).
                <br><br>
              <b>7.</b> The European Army is financed by the European Union’s budget.
            </p>
