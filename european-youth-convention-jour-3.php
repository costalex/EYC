            <h1>Un torrent d’idées neuves pour l’Europe </h1>
            <br><br>
            <p><strong>Strasbourg, 3e jour de la Convention européenne de la Jeunesse</strong>
              <br>
              A Strasbourg, les 150 participants de la Convention européenne de la jeunesse entament leur troisième jour de travail. Leur objectif : écrire une Constitution pour l’Europe. La première version du texte final, dévoilée ce matin et amendée toute la journée.
              <br><br>
              <h2>L’histoire d’un travail d’équipe</h2>
              Les rapporteurs de la Constitution ont passé la nuit à compiler, organiser et rédiger les propositions formulées dans la journée de vendredi par l’assemblée. A leur réveil les participants ont investi les locaux du Conseil de la Région Grand Est pour entamer les derniers tours de table. Entre débriefing global et travail en équipes réduites, ils débattent désormais de propositions concrètes. Florilège : une République continentale, universalisme de l’Union, défense non-offensive, …
              <br><br>
              <h2>La Constitution de tous les européens</h2>
              Aidés par la méthode Syntégration©, les participants ont pris confiance et ont gagné en expertise sur les sujets abordés. Et ça se voit. Les débats sont plus vifs, plus éclairés et sont tous orientés dans le but de contribuer de la meilleure des manières à un texte qui veut définir l’Union européenne de demain. A la pause déjeuner, les participants se confient sur le travail réalisé dans la matinée. Martin témoigne : {« avec méthode, nous avons défini les grands principes d’une Union qui bénéficie à tous les européens. Nous avons pesé chaque mot pour représenter chaque citoyen et prendre en compte les aspirations de chacun. »} Texte constitutionnel oblige, les propositions formulées sont générales. Mais elles sont fondées sur les expériences de chacun et prennent en compte les aspirations de tous. Bien être, égalité, accomplissement personnel sont des sujets particulièrement débattus.
              <br><br>
              <h2>Des idées pour faire avancer l’Europe</h2>
              Le but est commun, mais les manières de l’atteindre sont diverses. Si certains points font déjà consensus, d’autres ont toujours besoin de trouver une majorité chez les participants. Sans surprise, le texte final contiendra la généralisation d’Erasmus à tous les cursus scolaires, bien au-delà de l’université. Il apparaît également clair que la Constitution européenne de la Jeunesse demandera l’harmonisation des politiques étrangères au niveau européen et impliquera la création d’une armée européenne unique. L’Europe sociale est également au cœur des préoccupations. Mais jusqu’où faut-il aller ? Créer un salaire minimum européen semble un minimum, mais cela nécessite-t-il de concevoir un système de retraite européen ? Idem pour la protection de l’environnement : on est d’accord qu’elle doit être promue, mais doit-on aller jusqu’à l’inclure dans le préambule de la Constitution, pour lui donner une importance plus grande encore ?
              <br><br>
              Et bien sûr, la question ultime : faut-il continuer à avancer tous ensemble à 27 pays, ou créer une Europe à plusieurs vitesses ? Sur ce point surtout, les débats promettent d’être encore vifs. Mais les participants le disent avec le sourire {« tous ensemble nous obtiendront une Constitution qui nous ressemble, la constitution que nous voulons »}.
