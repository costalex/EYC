            <h1>La Convention européenne de la Jeunesse est formée ! <br>150 citoyens se réuniront à Strasbourg en mars pour donner une Constitution à l'Europe</h1>
            <h4>Plus de 350 jeunes représentants de la société civile de 39 pays en Europe ont candidaté pour faire partie des 150 délégués de la Convention. Les participants se réuniront du 9 au 12 mars prochain au Parlement européen à Strasbourg avec une mission : donner une constitution à l’Europe.</h4>
            <br><br>
            <p><strong>Strasbourg, 16 Février 2017</strong> – <strong>Refonder l'Europe</strong>
              <br>
              Les citoyens qui constituent cette assemblée considèrent que l’Europe se trouve à un tournant de son histoire et qu’il est nécessaire de refonder l’Union européenne, sur une base plus démocratique et proche de ses citoyens. C’est l’axiome de base qui amènera cette assemblée à la rédiger en 4 jours une Constitution citoyenne pour l’Europe.
              <br><br>
              <em>« L’Union européenne ne s’est pas dotée d’instruments suffisants pour faire face aux crises qu’elle a dû et doit toujours surmonter. Par conséquent, elle est en passe de perdre tout soutien populaire sur le continent. Avec la Convention, les jeunes prouvent qu’une meilleure Europe est possible et qu’une constitution citoyenne est la clé pour y parvenir. » Jérôme Quéré</em>
              <br><br>
              Au sein de l’assemblée on retrouve, entre autres, des représentants d’organisations majeures comme l’UNICEF, Youth Presse ou Anti-Corruption International ainsi que les partis politiques des jeunes.
              <br><br>
              <strong>Une méthode de travail innovante conduite par un institut de recherche</strong>
              <br>
              La Convention Européenne de la Jeunesse a pour ambition de prouver qu’une refonte du projet européen est possible. Les débats rompent volontairement avec les formats traditionnels pour faire émerger des idées neuves. <em>“Nous avons noué un partenariat avec le Malik Management Institute, qui a développé une nouvelle méthode de négociation, la Syntegration®. Cette méthode intense et inclusive permet de tirer le meilleur parti du groupe et de produire en 4 jours les résultats de 3 mois de travail”</em> poursuit Jérôme Quéré.
